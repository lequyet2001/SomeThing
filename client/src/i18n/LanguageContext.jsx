const LanguageContext = createContext(null)

function formatTranslation(template, values) {
  if (!values) return template
  return Object.entries(values).reduce(
    (text, [key, value]) => text.replaceAll(`{${key}}`, value),
    template,
  )
}

export function LanguageProvider({ children }) {
  const [language, setLanguageState] = useState(() => localStorage.getItem(LANGUAGE_KEY) || 'vi')

  useEffect(() => {
    document.documentElement.lang = language
  }, [language])

  function setLanguage(nextLanguage) {
    setLanguageState(nextLanguage)
    localStorage.setItem(LANGUAGE_KEY, nextLanguage)
    document.documentElement.lang = nextLanguage
  }

  function toggleLanguage() {
    setLanguage(language === 'vi' ? 'en' : 'vi')
  }

  const value = useMemo(
    () => ({
      language,
      setLanguage,
      toggleLanguage,
      t: (key, values) => formatTranslation(translations[language]?.[key] || translations.vi[key] || key, values),
    }),
    [language],
  )

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (!context) {
    throw new Error('useLanguage must be used within LanguageProvider')
  }
  return context
}
